package sec01_datatype.EX01_UsageOfDataType;

/*변수의 선언 및 값 대입을 위한 두 가지 방법*/

public class UsageOfDataType {	
    public static void main(String[] args) {		
        //변수의 선언과 값의 대입을 동시에 하는 방법
        int a = 3;		
        //변수의 선언 + 값의 대입
        int b;
        b = 4;
        
        System.out.println(a);
        System.out.println(b);
    }	
}
